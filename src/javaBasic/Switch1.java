package javaBasic;

public class Switch1 {
	
	public static void main(String[]args) {
		

		String name = "kamal";
		switch (name) {
		case "rita":
			System.out.println("rita is printed");
			break;
		case "arun":
			System.out.println("arun is printed");
			break;
		case "kamal":
			System.out.println("kamal is printed");
			break;
		case "kushal":
			System.out.println("kushalis printed");
			break;
		case "shova":
			System.out.println("shova is printed");
		default:
			System.out.println("default value");

	}

	}
}
